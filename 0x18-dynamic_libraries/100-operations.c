/**
 * add - add two integers.
 * @a: first integer
 * @b: second integer
 *
 * Return: the results of the operation.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two integers.
 * @a: first integer
 * @b: second integer
 *
 * Return: the results of the operation.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two integers.
 * @a: first integer
 * @b: second integer
 *
 * Return: the results of the operation.
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two integers.
 * @a: first integer
 * @b: second integer
 *
 * Return: the results of the operation.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - returns modulus of two integers.
 * @a: the first integer.
 * @b: the second integer.
 *
 * Return: the results of the operation.
 */
int mod(int a, int b)
{
	return (a % b);
}
